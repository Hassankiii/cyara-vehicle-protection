export const CAR_CATALOG = Object.fromEntries(
  Object.keys(CAR_DB).sort().map(make => [
    toTitleCase(make),
    Object.keys(CAR_DB[make]).map(m => toTitleCase(m)).sort()
  ])
)

// ── Dimension lookup ──────────────────────────────────────────────────────────
export function lookupDimensions(make, model, year) {
  const makeKey  = make.toLowerCase().trim()
  const modelKey = model.toLowerCase().trim()
  const yearStr  = String(year)
  const makeData = CAR_DB[makeKey]
  if (!makeData) return null

  let modelData = makeData[modelKey]
  if (!modelData) {
    for (const k of Object.keys(makeData)) {
      if (k.includes(modelKey) || modelKey.includes(k)) { modelData = makeData[k]; break }
    }
  }
  if (!modelData) return null

  if (modelData[yearStr]) {
    const [l, w, h] = modelData[yearStr]
    return { length_mm: l, width_mm: w, height_mm: h, source: 'database' }
  }

  const yrs = Object.keys(modelData).map(Number).sort((a, b) => a - b)
  const target = parseInt(yearStr)
  let nearest = null, minDiff = Infinity
  for (const y of yrs) {
    const d = Math.abs(y - target)
    if (d < minDiff && d <= 3) { minDiff = d; nearest = y }
  }
  if (nearest) {
    const [l, w, h] = modelData[String(nearest)]
    return { length_mm: l, width_mm: w, height_mm: h, source: 'database', yearNote: `Using ${nearest} dims` }
  }
  return null
}

