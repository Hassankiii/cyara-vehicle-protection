const ACRONYMS = new Set(['bmw','gle','gls','glc','amg','lx','gx','rx','es','ls','qx80','qx60','q50','xt6','sf90','f8'])

function toTitleCase(str) {
  return str.split(' ').map(w =>
    ACRONYMS.has(w.toLowerCase()) ? w.toUpperCase() : w.replace(/\b\w/g, c => c.toUpperCase())
  ).join(' ')
}

